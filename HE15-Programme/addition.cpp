#include <iostream>
using namespace std;


int main()
{
	double zahl1 = 50;
	double zahl2 = 20;
	double ergebnis;
	double ergebnis1;
	double ergebnis2;
	double ergebnis3;


	ergebnis = zahl1 + zahl2;
	ergebnis1 = zahl1 - zahl2;
	ergebnis2 = zahl1 * zahl2;
	ergebnis3 = zahl1 / zahl2;

	cout <<"Das Ergebnis der Adition von 50 und 20 ist: "<<ergebnis<<endl;
	cout <<"Das Ergebnis der Subtraktion von 50 und 20 ist: "<<ergebnis1<<endl;
	cout <<"Das Ergebnis der Multiplikation von 50 und 20 ist: "<<ergebnis2<<endl;
	cout <<"Das Ergebnis der Division von 50 und 20 ist: "<<ergebnis3<<endl;

return 0;
}

